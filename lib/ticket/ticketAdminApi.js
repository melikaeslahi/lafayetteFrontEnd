import { createApi, fetchBaseQuery } from '@reduxjs/toolkit/query/react';
import { Result } from 'postcss';
import { HYDRATE } from 'next-redux-wrapper'



export const  ticketAdminApi = createApi({
    reducerPath: 'ticketAdminApi',
    baseQuery: fetchBaseQuery({ baseUrl: `${process.env.NEXT_PUBLIC_BACKEND_URL}/api/v1/admin/ticket/admin` ,
    prepareHeaders: (headers, { extra }) => {
        headers.set('X-XSRF-TOKEN' , extra.cookie) // cookies is the name of the field added to the extraArgument
     
         return headers
       }, }),
    extractRehydrationInfo(action, { reducerPath }) {
        if (action.type === HYDRATE) {
            return action.payload[reducerPath]
        }
    },
    tagTypes: ['TicketAdmin'],

    // headers:{
    //     'Access-Control-Allow-Origin': '*',
    //        'Content-Type' :'multipart/form-data'
    // },
    credentials: true,

    endpoints: (builder) => ({
  
        getAllTicketAdmin: builder.query({
            query: (arg) => {
                const { page = 1, perPage = 0, search } = arg;
                return {
                    url: `${perPage}/${search}`,
                    params: { page },
                }
            },
            providesTags: ['TicketAdmin'],
    

        }),
        changeTicketAdminStatus: builder.mutation({
            query: (id) => `set/${id}`,

            invalidatesTags: ['TicketAdmin']
        }),
        
     


    



    })
});
export const { 
        useGetAllTicketAdminQuery,
        useChangeTicketAdminStatusMutation,
        
    } =  ticketAdminApi;


        
